class Node:
    def __init__(self, value):
        self.value = value
        self.next = None


class LinkedList:
    def __init__(self, value):
        new_node = Node(value)
        self.head = new_node
        self.tail = new_node
        self.length = 1

    def append(self, value):
        new_node = Node(value)
        if self.length == 0:
            self.head = new_node
            self.tail = new_node
        else:
            self.tail.next = new_node
            self.tail = new_node
        self.length += 1
        return True


    def has_loop(self):
        slow = self.head
        fast = self.head
        while fast is not None and fast.next is not None:
            slow = slow.next
            fast = fast.next.next
            if slow == fast:
                return True
        return False
    def has_loop_practice(self):
        if self.head.next is None:
            return False

        slow = self.head
        fast = self.head.next

        while fast is not None and fast.next is not None and slow != fast:
            slow = slow.next
            fast = fast.next.next

        return slow == fast

my_linked_list_1 = LinkedList(1)
my_linked_list_1.append(2)
